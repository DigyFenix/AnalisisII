    public static void agregarFrameADescktop(JInternalFrame frame) {
        try {
            JInternalFrame[] frames = escritorio.getAllFrames();
            boolean existe = false;
            for (JInternalFrame f : frames) {

                if (f.getTitle().equals(frame.getTitle())) {
                    DFOptionPane.mostrar_Realizado(frame.getTitle() + ",  ya se encuentra abierta");
                    existe = true;
                    break;
                }
            }
            if (!existe) {
                escritorio.add(frame);
                frame.setVisible(true);
                Dimension desktopSize = escritorio.getSize();
                Dimension FrameSize = frame.getSize();
                frame.setLocation((desktopSize.width - FrameSize.width) / 2, (desktopSize.height - FrameSize.height) / 2);
                frame.show();
            }

        } catch (Exception e) {
            DFOptionPane.mostrar_Error(e.getMessage(), "");
        }
    }

