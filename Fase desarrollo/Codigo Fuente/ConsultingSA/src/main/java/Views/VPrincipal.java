    public static void generarExcel(String sqlQuery, String nombreArchivo) {
        try {

            EntityManager em = HbUtils.getEntityManager();
            em.getTransaction().begin();
            Session session = em.unwrap(Session.class);
            session.doWork(new Work() {
                @Override
                public void execute(Connection cnctn) throws SQLException {
                    try (PreparedStatement pstm = cnctn.prepareStatement(sqlQuery)) {
                        ResultSet rs = pstm.executeQuery();
                        List<String[]> lista = new ArrayList<>();
                        String encabezados[] = {};
                        int cont = 0;
                        while (rs.next()) {
                            if (cont == 0) { //Genero los encabezados en automatico
                                encabezados = new String[rs.getMetaData().getColumnCount()];
                                for (int i = 1; i <= rs.getMetaData().getColumnCount(); i++) {
                                    encabezados[i - 1] = rs.getMetaData().getColumnName(i);
                                }
                                cont = 1;
                            }
                            String fila[] = new String[rs.getMetaData().getColumnCount()];
                            for (int i = 1; i <= rs.getMetaData().getColumnCount(); i++) {
                                fila[i - 1] = rs.getString(i);
                            }
                            lista.add(fila);
                        }
                        DFExcel.generar_excel(encabezados, lista, nombreArchivo, "Datos");
                    } catch (Exception e) {
                        DFOptionPane.mostrar_Error(e.toString());
                    }
                }
            });
            em.getTransaction().commit();
            em.close();

        } catch (Exception e) {
        }
    }

    private void cargarImagen(javax.swing.JDesktopPane jDeskp, File fileImagen) {
        try {
            BufferedImage image = ImageIO.read(fileImagen);
            jDeskp.setBorder(new ImagenFondo(image));
        } catch (Exception e) {
            System.out.println("Imagen no disponible");
        }
    }

